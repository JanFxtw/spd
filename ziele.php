        <p>
          <b>Dr. Albrecht Schröter - Programm für die Jahre 2018 – 2024</b>
          <p>
            Seit nunmehr 12 Jahren bin ich dank Ihres Vertrauens Oberbürgermeister der Stadt Jena. Freude und Achtung vor der Verantwortung haben mich in diesem Amt immer begleitet. Ich bin dankbar, in diesem Dienst für unsere Stadt arbeiten zu dürfen.
            </br></br>
            Die Jahre waren äußerst arbeitsintensiv, herausfordernd, aber auch erfolgreich. Dank einer zuverlässigen Stadtpolitik, einer erfolgreichen Wirtschaft, einer kreativen Bildungslandschaft und nicht zuletzt dank zuverlässiger und engagierter Mitstreiterinnen und Mitstreiter in allen Bereichen der Stadt und der Verwaltung steht Jena vor einem weiteren großen Aufschwung und wird sich noch stärker als sehr erfolgreiche deutsche Stadt etablieren.
            </br></br>
            Nach Jahren, in denen wesentliche Projekte intensiv vorbereitet wurden, ist jetzt die Zeit der Realisierung gekommen. Dank dieser positiven Entwicklung kann die Stadt ihren Bürgerinnen und Bürgern für ihren Beitrag zum Erfolg etwas zurückgeben. Ich werde mich weiterhin dafür einsetzen, dass alle Menschen in unserer Stadt am Erfolg teilhaben.
            </br></br>
            Mit Ihnen gemeinsam möchte ich unsere Stadt weiter voranbringen und vielversprechend Begonnenes zum erfolgreichen Abschluss führen. Mit Leidenschaft, Engagement und Verlässlichkeit möchte ich weiter für Jena - unsere Stadt - arbeiten.
          </p>
          Deshalb bitte ich Sie erneut um Ihr Vertrauen!
        </p>
        </br>
        <p>
          <b>10 Ziele für weitere sechs Jahre</b>
          </br>
          Klicken Sie für mehr Informationen einfach auf die einzelnen Ziele.
        </p>
        <p>
          <details>
            <summary>
              <b>1. Stadt- und Verkehrsentwicklung ist Ausdruck unserer Wachstumsdynamik</b>
            </summary>
            <p>
              </br>
              <b>Unser Zentrum steht vor einem Entwicklungsschub, wie wir ihn seit den 1990er Jahren nicht hatten.</b>
              </br></br>
              Jenas Zentrum wird als Ort für Wohnen, Einkauf, Wissenschaft und Gewerbe neu gestaltet. Der Inselplatz wird mit dem neuen Universitätscampus bebaut, der Engelplatz erhält mit der künftigen Stadtbibliothek und dem Bürgerservice ein neues Gesicht.
              </br></br>
              Dank breiter Einbeziehung der Bürgerinnen und Bürger wird der Eichplatz ab Beginn der 2020er Jahre bebaut. Das Volkshaus wird zum Kongresszentrum weiterentwickelt, die Neugestaltung des Bachstraßenareals nach Auszug des Universitätsklinikums rückt in greifbare Nähe. Dieser beispiellose Bauboom ist Ausdruck der starken Entwicklung unserer Stadt. Die Bürgerinnen und Bürger müssen sich mit diesen Projekten identifizieren können und tatsächlich hinter ihnen stehen. Damit dies gelingt, werde ich sie weiter mit einbeziehen. Ein wichtiges Instrument dafür ist der neue Beirat für Bürgerbeteiligung.
              </br></br>
              Das Straßennetz braucht eine kontinuierliche Ertüchtigung und - angesichts des Zuwachses an Beschäftigen in unserer Stadt - einen effektiven und modernen Ausbau. Die Erweiterung der Osttangente beginnt 2019. Für den Ausbau der Wiesenstraße nach Norden werde ich mich weiterhin ebenso einsetzen wie für die Ortsumgehung in Isserstedt - beides Maßnahmen, für die allerdings der Freistaat Thüringen verantwortlich ist.Mit der schrittweisen Umrüstung zum Einsatz von Elektromobilität wird sich die Stadt auf den Verkehr von morgen einstellen.
              </br></br>
              Jena wird den dringend notwendigen IC-Fernverkehrsknoten als modernen, gut angebundenen Bahnhof bekommen. Fern- und Regionalverkehr sollen gleichermaßen entwickelt werden, um die Erreichbarkeit des Wirtschafts-, Bildungs- und Wissenschaftszentrum Jena noch weiter zu verbessern.
              </br></br>
              Gutes Wachstum kann nur im Einklang mit unserer Umwelt nachhaltig gelingen. <b>Mir ist wichtig, das Jena weiterhin vorbildlich Klimaschutz betreibt.</b> Denn längst sind erneuerbare Energieträger nicht nur klimaschonend, sondern wirtschaftlich gewinnbringend für unsere Stadt. Das darf nicht aufs Spiel gesetzt werden, auch angesichts unserer weltweiten Verantwortung. Daher ist es gut, dass die Stadtwerke Jena schon heute 100 % Strom aus erneuerbaren Energien anbieten. Diesen Weg des aktiven Klimaschutzes möchte ich fortsetzen.
              </br>
            </p>
          </details>
        </p>
        <p>
          <details>
            <summary>
              <b>2. Familien, Wohnen und Mobilität gehören zusammen</b>
            </summary>
            <p>
              </br>
              Zu Jenas Markenzeichen gehört die Familienfreundlichkeit. Diese werde ich noch weiter ausbauen. Die Zahl der Kinder wächst in erfreulicher Weise. Sie sollen möglichst wohnortnah in Kitas und Schulen gehen können.
              </br></br>
              Dazu bedarf es zusätzlicher Investitionen. Die kostenfreie Nutzung des öffentlichen Nahverkehrs für alle ist ein Fernziel, auf das Jena perspektivisch und schrittweise hinarbeiten sollte. Als ein wichtiges Etappenziel werde ich eine kostenfreie Nutzung für Kinder und Jugendliche in den nächsten drei Jahren und den notwendigen Ausbau der Bus- und Bahninfrastruktur anstreben. Das wäre nicht nur für den Weg zur Schule optimal, sondern prägend für ein umweltbewusstes Leben.
              </br></br>
              Ein weiterer möglicher Zwischenschritt ist die Einführung der in anderen Städten bereits erfolgreich genutzten 365-Euro-Jahreskarte (1 Euro pro Tag), um eine kostengünstige Nutzung des Nahverkehrs zu gewährleisten. Denn Mobilität fördert Teilhabe am gesellschaftlichen Leben für alle.
              </br></br>
              <b>Ich setze mich dafür ein das Bedingungen für ein altersgerechtes Wohnen und Leben angesichts der Lebensleistung von Senioren hohe Priorität haben.</b>
              </br></br>
              Die Stadt wird auch weiterhin die Voraussetzungen für einen umfangreichen Wohnungsbau schaffen, der dem Wachstum der Bevölkerung Rechnung trägt und die Dämpfung des Mietniveaus unterstützt. Bedarfsgerechte Angebote sowie bezahlbarer Wohnraum für jedes Einkommen sind dabei wesentliche Kriterien.
              </br>
            </p>
          </details>
        </p>
        <p>
          <details>
            <summary>
              <b>3. Bildung hält junge Menschen in der Region</b>
            </summary>
            <p>
              </br>
              <b>Jenas Bildungslandschaft ist deutschlandweit führend.</b> Kaum eine andere deutsche Stadt kann von sich sagen, dass nahezu alle Schulgebäude vorbildlich saniert sind. Letzte ausstehende Sanierungen wie die der Schule An der Trießnitz werden bald abgeschlossen sein. Lernen soll in Jena künftig noch mehr Spaß machen. Der Neubau der Gemeinschaftsschule in Jena-Ost ist ein Meilenstein und wird die Jenaer Schullandschaft nachhaltig prägen.
              </br></br>
              Das erfolgreiche Modell kommunaler Schulen wird fortgesetzt wie auch das vielfältige Angebot in der Jugendsozialarbeit. Inklusion und Ausbau der Medienkompetenzen sollen weiter gefördert werden.
              </br></br>
              Die effektive Vernetzung von Schule, Berufsausbildung, Hochschulen und Wirtschaft als künftige Arbeitgeberin ist eine entscheidende Voraussetzung dafür, dass junge Menschen in Jena und in der Region bleiben. In Jena gibt es genügend Ausbildungsplätze. <b>Die Chancen, eine Arbeit zu finden, sind sehr hoch.</b>
              </br></br>
              <b>Wissenschaft und Forschung sind entscheidende Motoren für die Weiterentwicklung des Innovationsstandortes Jena.</b> Die Förderung der Hochschulen und Forschungseinrichtungen durch Optimierung ihrer Rahmenbedingungen bleibt eines meiner wichtigsten Ziele. Das Wohlergehen der Wissenschaft ist für Jena zentral. Dazu zählt auch die Unterstützung bei der Wohnraumbereitstellung für Studierende sowie Wissenschaftlerinnen und Wissenschaftler.
              </br>
            </p>
          </details>
        </p>
        <p>
          <details>
            <summary>
              <b>4. Kultur und Natur - Ausdruck hoher Lebensqualität</b>
            </summary>
            <p>
              </br>
              <b>Jena hat eine hervorragende Kunst- und Kulturlandschaft - von der Hochkultur bis zur Szene- und Soziokultur.</b> Die Finanzen der Jenaer Kultureinrichtungen sind solide und zukunftssicher aufgestellt. Die Philharmonie wird als einziges deutsches Orchester im europäischen Orchesternetzwerk ("ONE") und durch die erfolgreich eingeworbenen Finanzmittel aus dem Programm "Exzellente Orchesterlandschaft" der Bundesregierung ihre Qualität und ihre internationale Strahlkraft weiter steigern können.
              </br></br>
              Das neue Deutsche Optische Museum wird auch dank städtischer Unterstützung in den nächsten Jahren sichtbare Konturen annehmen. Großartige Events wie die Kulturarena werden weiterentwickelt und auch künftig unsere Sommer verschönern.
              </br></br>
              Lebensqualität wird ganz wesentlich durch die Pflege der Landschaftskultur bestimmt. Jena ist bekannt für seine herrliche Lage entlang der Saale. Unser attraktiver Landschaftsraum wird weiterhin und für eine nachhaltige Nutzung erschlossen, u.a. mit einer erlebbaren Flusslandschaft in Parks, einem gut ausgebauten Radwegenetz sowie einer erwanderbaren Wald- und Berglandschaft.
              </br>
            </p>
          </details>
        </p>
        <p>
          <details>
            <summary>
              <b>5. Sportförderung für alle</b>
            </summary>
            <p>
              </br>
              Breiten- und Vereinssport sind gleichermaßen wichtig, weil sie der Gesundheit dienen und das bunte, vielfältige Leben unserer Stadt ganz wesentlich prägen. Sie sollen weiter gefördert und unterstützt werden. Dazu zählt auch, dass dem Wunsch der Jenaerinnen und Jenaer nach einer <b>erhöhten Schwimmhallenkapazität</b> Rechnung getragen wird.
              </br></br>
              Insbesondere seit dem Aufstieg des FC Carl Zeiss Jena in die 3. Liga motiviert auch der Fußball wieder stärker. <b>Ein neues Stadion wird kommen.</b> Die Ausschreibung hierfür läuft. Auch andere Profi- und Leistungssportler verdienen als hervorragende Werbeträger unserer Stadt bestmögliche Unterstützung - so zum Beispiel die Basketballer von "Science City", die Leichtathleten oder die Badminton-Mannschaft vom "GuthsMuths“-Verein.
              </br>
            </p>
          </details>
        </p>
        <p>
          <details>
            <summary>
              <b>6. Wirtschaftlichen Aufschwung verstetigen, Fachkräfte gewinnen</b>
            </summary>
            <p>
              </br>
              <b>Jena als Wissenschafts- und Technologiestandort hat weltweite Ausstrahlung.</b> Der Jenaer Wirtschaft geht es gut. Sie investiert. Der Bau des ZEISS-Innovationscampus nahe dem Westbahnhof für 300 Mio. € wird allein 500 neue Arbeitsplätze nach sich ziehen.
              </br></br>
              Everpharma investiert 100 Mio. €. Das neue Jenaer Institut des Deutschen Zentrums für Luft- und Raumfahrt wird in einen Neubau investieren. <b>Insgesamt werden bis 2024 insgesamt mindestens € 700 Mio. zum Bau von Bildungs- und Forschungseinrichtungen, Wohnung und Wirtschaftsunternehmen in unsere Stadt investiert.</b>
              </br></br>
              <b>Das zeigt den hervorragenden Ruf, den sich die Stadt durch ihre erfolgreiche Politik erworben hat.</b>
              Damit dieser Aufschwung nachhaltig trägt, braucht es viele Fachkräfte. Ich werde auch weiterhin die Förderung insbesondere des naturwissenschaftlichen Lernens an den Schulen unterstützen und der Schaffung bester Rahmenbedingungen für die Fachkräftegewinnung größte Aufmerksamkeit schenken.
              </br>
            </p>
          </details>
        </p>
        <p>
          <details>
            <summary>
              <b>7. Neu-Lobeda, Winzerla und kleinere Ortsteile stärken</b>
            </summary>
            <p>
              </br>
              <b>Die Großwohnsiedlungen im Jenaer Süden sind prägender Bestandteil unserer Stadt - entsprechend erfolgreich entwickeln sie sich.</b> Der Klinikneubau in Neu-Lobeda mit allen Folgeinvestitionen verändert das dortige Stadtbild und verstärkt die dynamische Entwicklung des lebendigen und attraktiven Stadtteils.
              </br></br>
              Winzerla hat sich ebenfalls sehr gut entwickelt. Der Stadtteil braucht zeitnah ein Zentrum als Heimstatt für die vielen Vereine und Initiativen vor Ort. Dafür werde ich mich einsetzen. In beiden Stadtteilen werden auch weiterhin alle Schulformen angeboten, um lange Schulwege zu vermeiden.
              </br></br>
              Der in den nächsten Jahren stark wachsende <b>Jenaer Norden wird durch den Ausbau der Straßenbahn besser an das ÖPNV-Netz angebunden.</b>
              Jena-Zentrum erhält durch die moderne und intelligente Bebauung des Eichplatzareals <b>eine attraktive neue Mitte.</b>
              </br></br>
              Die kleineren Ortsteile mit ihren eigenen Traditionen und Veranstaltungen werden weiterhin als sympathische Alternative zum urbanen Zentrum Jenas gepflegt.
              </br>
            </p>
          </details>
        </p>
        <p>
          <details>
            <summary>
              <b>8. Entschuldung abschließen, Verwaltung modernisieren und digitalisieren</b>
            </summary>
            <p>
              </br>
              Bis spätestens 2024 wird die Entschuldung Jenas dank einer soliden Haushaltspolitik und einer erfolgreichen Wirtschaft abgeschlossen sein. <b>Das eröffnet mehr Spielräume für soziale Projekte und für Investitionen.</b>
              </br></br>
              Moderne Verwaltung als Dienstleister für die Bürgerinnen und Bürger heißt in den kommenden Jahren vor allem Ausbau der Digitalisierung. Das öffentliche WLAN wird ausgeweitet, um möglichst flächendeckend verfügbar zu sein.
              </br></br>
              <b>Der neue Internetauftritt der Stadt wird Ende 2018 freigeschaltet und die Ansprüche von Barrierefreiheit und Mehrsprachigkeit berücksichtigen.</b> Immer mehr Anliegen werden online erledigt werden können, wie bereits jetzt schon die <b>Anmeldung für Kitaplätze</b> oder in naher Zukunft die <b>Beantragung von Vereinszuschüssen.</b> Ungeachtet dessen wird in den Neubau des <b>Bürgerservice und der Stadtbibliothek am Engelplatz/Neugasse investieren</b>, damit ein persönlicher Kontakt zu den Nutzerinnen und Nutzern erhalten bleibt.
              </br>
            </p>
          </details>
        </p>
        <p>
          <details>
            <summary>
              <b>9. Zivilgesellschaft fördern, Menschen mit Migrationshintergrund gut integrieren</b>
            </summary>
            <p>
              </br>
              Die Bürgerinnen und Bürger Jenas haben in den letzten Jahren wiederholt antidemokratischen Aufmärschen erfolgreich ihren kreativen Widerstand entgegengesetzt. <b>Für dieses Engagement bin ich außerordentlich dankbar.</b>
              </br></br>
              <b>Ich werde mich weiterhin persönlich dafür engagieren, dass Rechtspopulismus und Fremdenfeindlichkeit in Jena keine Chance haben. Jena war, ist und bleibt eine weltoffene Stadt.</b>
              </br></br>
              Das zeigt sich nicht zuletzt in der fortlaufenden Integrationsarbeit für Menschen mit Migrationshintergrund, insbesondere für geflüchtete Menschen, die mit großem Engagement von ehrenamtlich Tätigen mitgetragen wird. Die Stadt wird dies weiterhin intensiv unterstützen.
              </br>
            </p>
          </details>
        </p>
        <p>
          <details>
            <summary>
              <b>10. Regionale Anbindung und Kooperation ausbauen</b>
            </summary>
            <p>
              </br>
              Jena muss seine sehr guten Beziehungen zu den Nachbargemeinden, zum Saale-Holzland-Kreis und zum Kreis Weimarer Land weiter ausbauen und zum wechselseitigen Wohlergehen nutzen. <b>Attraktive Wohnbau- und Gewerbeflächen mit guter Straßen- und ÖPNV-Anbindung an die Stadt stärken die erfolgreiche Entwicklung der ganzen Region.</b>
              </br></br>
              Ein starkes Jena braucht eine starke kommunale Gemeinschaft. Deshalb werde ich mich weiterhin für den <b>Ausbau der Impulsregion Mittelthüringen und der Europäischen Metropolregion Mitteldeutschland</b> einsetzen.
              </br>
            </p>
